function Stepper({ step }) {
  const LABELS = ["", "Request", "In Progress", "Review", "Completed"];
  return (
    <div className="flz-stepper-wrap">
      <div className="flz-track-bg" />
      <div className="flz-track-active" style={{ width: TRACK_W[step] }} />
      {[1, 2, 3, 4].map((i) => {
        let cls = "todo";
        if (i < step) cls = "completed";
        else if (i === step) cls = "active";
        return (
          <div key={i} className={`flz-step ${cls}`}>
            <div className="flz-step-circle">{i < step ? "✓" : `0${i}`}</div>
            <p>{LABELS[i]}</p>
          </div>
        );
      })}
    </div>
  );
}

// Step 3 card: file download + approve/revisi buttons
function FinalCard({ onApprove, onDecline }) {
  const handleDownload = (file) => {
    const blob = new Blob([file.content], { type: "application/zip" });
    const url = URL.createObjectURL(blob);
    const a = document.createElement("a");
    a.href = url;
    a.download = file.filename;
    a.click();
    URL.revokeObjectURL(url);
  };

  return (
    <div className="flz-final-card">
      <h2>Final Project Documentation</h2>
      <div className="flz-file-row">
        {FILES.map((f) => (
          <div className="flz-file-box" key={f.key}>
            <div className="flz-file-info">
              <h4>{f.name}</h4>
              <span>{f.type}</span>
            </div>
            <button className="flz-dl-btn" onClick={() => handleDownload(f)} title="Download">
              ⬇
            </button>
          </div>
        ))}
      </div>
      <div className="flz-action-row">
        <button className="flz-btn flz-btn-revisi" onClick={onDecline}>Revisi</button>
        <button className="flz-btn flz-btn-approve" onClick={onApprove}>✓ Approve</button>
      </div>
    </div>
  );
}

