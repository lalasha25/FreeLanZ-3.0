export default function ProjectPage({ onChangePage, currentPage }) {
  const [activeTab, setActiveTab] = useState("ONGOING");
  
  // Load initial projects from localStorage
  const [projects, setProjects] = useState(() => {
    const saved = localStorage.getItem('client_projects');
    if (saved) {
      try {
        return JSON.parse(saved);
      } catch (e) {
        console.error('Failed to parse client_projects:', e);
      }
    }
    return DEFAULT_PROJECTS;
  });

  const [selectedId, setSelectedId] = useState("anniversary");
  const [approved, setApproved] = useState(false);
  const [rated, setRated] = useState(false);
  const [ratedStars, setRatedStars] = useState(0);
  const [modal, setModal] = useState(null);
  const [showSuccessPopup, setShowSuccessPopup] = useState(false);
  const [currentUser, setCurrentUser] = useState(null);

  // Sync to localStorage
  useEffect(() => {
    localStorage.setItem('client_projects', JSON.stringify(projects));
  }, [projects]);

  useEffect(() => {
    const link = document.createElement("link");
    link.rel = "stylesheet";
    link.href = "https://cdnjs.cloudflare.com/ajax/libs/font-awesome/6.0.0/css/all.min.css";
    document.head.appendChild(link);

    const userStr = localStorage.getItem("user");
    if (userStr) {
      setCurrentUser(JSON.parse(userStr));
    }

    const handleStorageSync = (e) => {
      if (e.key === 'client_projects' && e.newValue) {
        try {
          setProjects(JSON.parse(e.newValue));
        } catch (err) {
          console.error(err);
        }
      }
    };
    window.addEventListener('storage', handleStorageSync);

    return () => {
      document.head.removeChild(link);
      window.removeEventListener('storage', handleStorageSync);
    };
  }, []);

  const selectedProject = projects.find((p) => p.id === selectedId);

  const getActiveStep = (p) => {
    if (!p) return 1;
    if (p.isMain && approved) return 4;
    return p.step;
  };

  const currentStep = getActiveStep(selectedProject);

  const handleConfirmApprove = () => {
    setApproved(true);
    setModal(null);
    setProjects(prev => prev.map(p => p.id === 'anniversary' ? {
      ...p,
      status: 'COMPLETED',
      st: 'cp',
      progress: 100,
      step: 4,
      cat: 'COMPLETED'
    } : p));
  };

  const handleGoToChat = () => {
    setModal(null);
    onChangePage?.("chat");
  };

  const handleRatingSubmit = (stars, _comment) => {
    setRatedStars(stars);
    setRated(true);
    setShowSuccessPopup(true);
  };

  const filtered = projects.filter((p) => p.cat === activeTab);

  return (
    <div className="flz-wrap">
      <style>{styles}</style>

      {/* HEADER */}
      <header style={{ width: '100%' }}>
        <nav style={{ display: 'flex', justifyContent: 'space-between', alignItems: 'center', padding: '15px 5%', height: '70px', backgroundColor: '#000066' }}>
          <div
            style={{ color: '#ccff00', fontSize: '26px', fontWeight: '900', fontStyle: 'italic', letterSpacing: '-1px', cursor: 'pointer' }}
            onClick={() => onChangePage?.('landing')}
            title="Kembali ke Landing Page"
          >FreeLanZ</div>
          <ul style={{ display: 'flex', listStyle: 'none', gap: '25px', margin: 0, padding: 0 }}>
            <li>
              <a
                href="#explore"
                style={{ textDecoration: 'none', color: currentPage === 'explore' ? '#ccff00' : 'white', fontSize: '13px', fontWeight: 'bold' }}
                onClick={(e) => { e.preventDefault(); onChangePage?.('explore'); }}
              >EXPLORE</a>
            </li>
            <li>
              <a
                href="#chat"
                style={{ textDecoration: 'none', color: currentPage === 'chat' ? '#ccff00' : 'white', fontSize: '13px', fontWeight: 'bold' }}
                onClick={(e) => { e.preventDefault(); onChangePage?.('chat'); }}
              >CHAT</a>
            </li>
            <li>
              <a
                href="#project"
                style={{ textDecoration: 'none', color: currentPage === 'project' ? '#ccff00' : 'white', fontSize: '13px', fontWeight: 'bold' }}
                onClick={(e) => { e.preventDefault(); onChangePage?.('project'); }}
              >PROJECT</a>
            </li>
          </ul>
          <div
            style={{ display: 'flex', alignItems: 'center', gap: '12px', fontSize: '13px', fontWeight: '600', color: 'white', cursor: 'pointer' }}
            onClick={() => onChangePage?.('profile')}
          >
            <span>{currentUser ? currentUser.name.toUpperCase() : "LINATA SATORO"}</span>
            <div style={{ width: '35px', height: '35px', backgroundColor: '#e0e0e0', borderRadius: '50%', display: 'flex', alignItems: 'center', justifyContent: 'center', color: '#333', overflow: 'hidden' }}>
              {currentUser && currentUser.avatarUrl ? (
                <img
                  src={currentUser.avatarUrl.startsWith('http') ? currentUser.avatarUrl : `http://localhost:5000${currentUser.avatarUrl}`}
                  alt="avatar"
                  style={{ width: '100%', height: '100%', objectFit: 'cover' }}
                />
              ) : (
                <i className="fas fa-user"></i>
              )}
            </div>
          </div>
        </nav>
      </header>

      <div className="flz-container">

        {/* HERO */}
        <div className="flz-hero">
          <div className="flz-badge">
            <span className="flz-badge-dot" />
            {currentStep === 4 ? "PROJECT COMPLETED" : "PROJECT ON TRACK"}
          </div>
          <div className="flz-hero-row">
            <div className="flz-hero-title">
              <h1>{selectedProject?.title ?? "—"}</h1>
              <p>Project #FLZ-2024-001 • Last Update: 10m ago</p>
            </div>
            <div className="flz-collab">
              <div className="flz-collab-info">
                <span>Collaboration with</span>
                <strong>{selectedProject?.collab ?? "—"}</strong>
              </div>
              <img src={`https://i.pravatar.cc/40?img=${selectedProject?.collabImg ?? 1}`} alt="collab" />
            </div>
          </div>
        </div>

        {/* STEPPER */}
        <div className="flz-stepper-card">
          <Stepper step={currentStep} />
        </div>

        {/* KONDISI BERDASARKAN STEP */}

        {/* Step 3: Final Project card (belum di-approve) */}
        {currentStep === 3 && (
          <FinalCard
            onApprove={() => setModal("approve")}
            onDecline={() => setModal("decline")}
          />
        )}

        {/* Step 4: Approved banner + Rating card */}
        {currentStep === 4 && (
          <>
            <ApprovedBanner project={selectedProject} />
            <RatingCard
              rated={rated}
              ratedStars={ratedStars}
              onSubmit={handleRatingSubmit}
              onCancel={() => onChangePage?.("project")}
            />
          </>
        )}

        {/* WORK PROJECT */}
        <div>
          <div className="flz-section-title">WORK PROJECT</div>
          <div className="flz-tabs">
            {["ONGOING", "COMPLETED"].map((tab) => {
              const count = projects.filter((p) => p.cat === tab).length;
              return (
                <div
                  key={tab}
                  className={`flz-tab${activeTab === tab ? " active" : ""}`}
                  onClick={() => { setActiveTab(tab); setSelectedId(null); }}
                >
                  {tab} ({count})
                </div>
              );
            })}
          </div>
          <div className="flz-proj-list">
            {filtered.map((p) => (
              <ProjectCard
                key={p.id}
                project={p}
                selected={selectedId === p.id}
                onClick={() => setSelectedId(p.id)}
              />
            ))}
          </div>
          {selectedProject && selectedProject.cat === activeTab && (
            <div className="flz-detail-box">
              <div className="flz-detail-hdr">
                <h3>DETAIL: {selectedProject.title}</h3>
                <button className="flz-close-btn" onClick={() => setSelectedId(null)}>[X]</button>
              </div>
              <p style={{ fontSize: 12, color: "#555" }}>{selectedProject.desc}</p>
              <div className="flz-detail-meta">
                <div><strong>Status:</strong> {selectedProject.status}</div>
                <div><strong>Progress:</strong> {selectedProject.progress}% Done</div>
                <div><strong>Deadline:</strong> {selectedProject.deadline}</div>
              </div>
            </div>
          )}
        </div>
      </div>

      {/* MODAL — APPROVE CONFIRM */}
      <Modal show={modal === "approve"}>
        <button className="flz-modal-close" onClick={() => setModal(null)}>×</button>
        <div className="flz-success-ring">✓</div>
        <div className="flz-modal-title">Approve this project?</div>
        <div className="flz-modal-sub">
          You are approving the work submitted {selectedProject?.collab}.<br />
          This project will be marked as <strong>Completed</strong>.
        </div>
        <button className="flz-modal-btn" onClick={handleConfirmApprove}>Yes, Approve!</button>
      </Modal>

      {/* MODAL — DECLINE / REVISI */}
      <Modal show={modal === "decline"}>
        <button className="flz-modal-close" onClick={() => setModal(null)}>×</button>
        <div style={{ fontSize: 38, marginBottom: 14 }}>📝</div>
        <div className="flz-modal-title">Request Revision?</div>
        <div className="flz-modal-sub">
          Declining will send this project back for revision.<br />
          A message will be sent to {selectedProject?.collab} via Chat.
        </div>
        <button className="flz-modal-btn-sec" onClick={() => setModal(null)}>Cancel</button>
        <button className="flz-modal-btn" onClick={handleGoToChat}>Go to Chat →</button>
      </Modal>

      {/* SUCCESS POPUP — after rating submit */}
      {showSuccessPopup && (
        <div className="flz-success-popup">
          <div className="flz-popup-icon">
            <i className="fas fa-check-circle" />
          </div>
          <h2 className="flz-popup-h2">Rating successfully submitted</h2>
          <p className="flz-popup-p">Terima kasih atas ulasan Anda!</p>
          <button
            className="flz-popup-btn"
            onClick={() => {
              setShowSuccessPopup(false);
              onChangePage?.("project");
            }}
          >
            Back to mainpage!
          </button>
        </div>
      )}
    </div>
  );
}